//引入库的概念：运算：java.lang.Math
package computation;

public class dataku {
	public static void main(String[] args)
	{
		//求次方
		double a = 2;
		double b = Math.pow(a, 6);
		System.out.println(b);
		//快捷键Ctrl+f11
		//平方根
		double c = Math.sqrt(b);
		System.out.println(c);
		//其他等待补充：pow,sqrt,random,sin,...
	}
}
